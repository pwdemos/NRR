            <h1 class="text-l text-center">
                North River is a mineral exploration and development company active in Namibia and Mozambique. The Company is focussed on re-opening its flagship asset, the Namib Lead & Zinc Mine, a fully permitted project near Swakopmund in Namibia and commenced construction in early 2018.<br/><br/>

The project has a current JORC resource of 1,120,000 tonnes, with resulting underground in situ metal inventory of:<br/><br/>

    25,900 tonnes of lead
    74,200 tonnes of zinc
    1.66 million ounces of silver

NRR proposes a staged mine production development, with a reduced construction capital, ramping up in accordance with the resource expansion resulting from ongoing exploration activities. The new implementation plan proposes a Q1 2018 construction start and with an initial production of 7,200t zinc and 2,500t lead in phase 1 of the project development, with future expansion planned.<br/><br/>

The construction period will be less than 1 year with an 8 year mine plan proposed based on a mining inventory of 885kt of ore mined.<br/><br/>

The project has a JORC compliant Probable Ore Reserve of 611,000t ore containing 6.6% zinc and 2.3% lead. The project received a 10-year Mining Licence from the Ministry of Mines and Energy in May 2017.<br/>    
					
