                        <h2>Directors & Management</h2><br/>
						<p><b>James Beams (Independent Director)</b></p>
Non-Executive Chairman<br/><br/>

James Beams joined North River having previously spent 14 years with Anglo American plc. For his final five years at Anglo American he was the Chief Financial Officer for the Copper division, and prior to that served in several senior management positions within the company. Prior to his time at Anglo American, he held senior finance roles at Minorco and Commercial Union.<br/><br/>

<br/>

<p><b>Mark Sawyer</b></p>
Non-Executive Director<br/><br/>

Mark Sawyer is a Senior Partner at Greenstone Capital LLP and a co-founder of Greenstone Resources LP ("Greenstone"), the private equity fund. Mark co-founded Greenstone in 2013 after a 18 year career in the mining sector including as co-head of group business development at Xstrata plc, and senior roles at Rio Tinto plc and Cutfield Freeman & Co Ltd. Together with Greenstone's co-founder, Mark oversees all aspects of the management of the business. He serves as a director of Greenstone Management Ltd, the fund's General Partner, and is a member, and co-Chairman, of the Investment Committee.<br/><br/>

<br/>

<p><b>Ken Sangster</b></p>
Non-Executive Director<br/><br/>

Ken Sangster was trained as a metallurgist and has 49 years' experience in the mining industry in a number of highly successful project development / project management roles ranging in project size from USD20M to USD600M. He previously worked in numerous senior roles for Rio Tinto plc for over 15 years, as well having metallurgy focused project development roles for Anglo American PLC, Consolidated Gold Fields, Outokumpu Metals and Resources, TVX Gold and Ivernia plc. He is also a Fellow of the Institute of Directors.<br/><br/>

<br/>

<p><b>Daniel Saint Don</b></p>
Non-Executive Director<br/><br/>

Dan has over 30 years of operational and technical experience in Canada, the United States and various international projects. This includes responsibilities in mine operations, engineering, contracting services, due diligence review, and project development. He is currently on the board of Graphex Mining Ltd. as a non-executive director and has held progressive roles of responsibility with Golder Associates, Atna Resources, DMC Mining Services, Boart Longyear Company, Stillwater Mining Company, Inmet Mining, and Mosaic, (formerly IMC). He holds a Bachelor of Science degree in Mining Engineering from Michigan Technological University, and an MBA from Westminster college in Salt Lake City. Daniel is a licensed professional engineer in Ontario, (P.Eng.).<br/><br/>

<br/>


<p><b>Ricardo De Armas</b></p>
Non-Executive Director<br/><br/>

Ricardo De Armas joined Castlelake in 2016 as part of the Emerging Markets team, focused on investments in the emerging markets. Prior to joining Castlelake, Mr. De Armas was a vice president at De Jong Capital, where he focused on deep value investments and provided restructuring and financial advisory. Prior to De Jong Capital, Mr. De Armas was a principal at Zaff Capital, where he focused on special situation investments in emerging markets. Prior to joining Zaff Capital, Mr. De Armas was an associate with Citigroup in the Latin American investment banking group in New York, where he advised companies in a wide range of industry sectors on financial and strategic matters. Prior to that, Mr. De Armas worked for Procter & Gamble in Caracas, Venezuela as a financial analyst and cost manager for a variety of brands in Latin America.<br/><br/>
<br/>

 
<p><b>Dag Kullmann</b></p>
Managing Director (Namibia)<br/><br/>

Dag Kullmann is a mining engineer with 28 years' experience in the mining industry, predominantly in Southern Africa. Before joining North River Resources in 2017, he spent 10 years in Namibia with the Forsys Metals Group of Companies, initially as project and mining manager, then also its general manager. Prior to this, Dag spent 18 years in South Africa, first working in the Wits gold fields and finally 8 years consulting with Snowden Mining Industry Consultants on a wide variety of commodities across Africa and further abroad. He acted as Qualified Person for several large and small mining companies for the completion of technical studies, reserve reporting, operational audits and listing documentation for a number of international stock exchanges.<br/><br/>
