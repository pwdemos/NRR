                        <h2>Namib Lead-Zinc Project</h2><br/><br/>
North River Resources holds 100% of the Namib Lead and Zinc Project (NLZM), which is centred on the underground Namib Lead Mine which was previously operational from 1968-1991.<br/><br/>

The project has a current JORC resource of 1,120,000 tonnes, with resulting underground in-situ metal inventory of:<br/><br/>

<ul>
    <li>25,900 tonnes of lead</li>
    <li>74,200 tonnes of zinc</li>
    <li>66 million ounces of silver</li>
</ul>
<br/>
The NLZM is located 30km from the town of Swakopmund and 75km from the port of Walvis Bay.<br/><br/>

Three ore bodies were mined previously, namely South, Junction and North ore bodies. The bulk of production was from the South and Junction ore bodies which were developed to 7.5 and 7 Level respectively (each Level being approximately 30m). The North ore body was developed to 2 Level.<br/><br/>

Additional occurrences of mineralisation are known at the N20 prospect and at numerous other surface gossan occurrences within the project area.<br/><br/>

The North River team has made considerable headway in obtaining legacy information relating to the operations of the mine from 1968 to 1991. This includes diamond drill data that was conducted during that period and prior to operations commencing in 1969. The information was obtained by the discovery of archival reports and plans from a number of different sources in Namibia, as well as interviews with individuals associated with the mine.<br/><br/>

Historically, the mine produced approximately 700,000 tonnes of ore (calculated from North River's 3D models of the mine voids), producing over 100,000 tonnes of lead and zinc concentrate, and over 1,000,000 ounces of silver. After net smelter returns, this would have yielded revenues in excess of $100 million at current commodity prices.<br/><br/>

The Company has access to bore hole logs and assay data relating to surface diamond drilling programmes conducted at Namib in 1956 and 1966, which demonstrate the potential for a number of intercepts up to 100 metres below the base of the historic mine workings, as well as some high grade intercepts at the Northern ore bodies.<br/><br/>

The lead-zinc mineralisation at NLZM and related prospects is thought to have been a primary stratabound sulphide deposit which has subsequently been metamorphosed and deformed. The intense metamorphism and deformation of the area has remobilised and re-deposited the primary sulphide mineralisation in structural traps which are however still largely stratabound.<br/><br/>

The mine is free of water and a total of 600 metres of channel sampling of key exposures has been completed. This is predominantly in the lowest parts of the mine in the Junction and South sections and on strike extensions to the mineralisation at higher levels of the mine.<br/><br/>

Since the completion of the Feasibility Study in late 2014, NLZM undertook several optimisation studies to improve the project economics and reduce associated development risks. These included several programmes of metallurgical testwork and infill diamond drilling to collect fresh ore samples for testing and better understand orebody variability and reduce the risks in the initial mining period. New Mineral Resource and Ore Reserve Statements were made by the respective consultants. NRR produced an updated mine development plan as an update to the previous feasibility study.
