                        <h2>Trading</h2><br/><br/>
						North River Resources Plc is a public company with a total issued share capital of 26,392,596 ordinary shares. Until 27 March 2017, the Company's shares were listed on the Alternative Investment Market ('AIM') of the London Stock Exchange. At a General Meeting held on the 17 March 2017, shareholders approved a resolution to cancel the admission of the shares from trading on AIM.<br/><br/>

The Company has established an electronic off-market dealing facility for the ordinary shares, which will be administered by Asset Match, a firm authorised and regulated by the Financial Conduct Authority. Asset Match provides a market platform for existing North River Resources Plc shareholders to realise the value of their share capital and also facilitates the purchase of North River Resources Plc shares for new investors. Shares are traded on a matched bargain and arm's length basis via periodic auctions to be held every three months.<br/><br/>

 

In order to trade shares, you will need to use a stockbroker. If you already have a stockbroker you can continue to trade shares through them. However, many people who trade shares on Asset Match do so through The Share Centre, www.share.com , one of the UK's leading retail stockbrokers. They will be able to help you buy, sell and monitor the value of your shares and keep up to date on Company announcements.<br/><br/>

 

AssetMatch.com shows bids to buy shares and offers to sell, with the share price set at each scheduled auction by calculating the single price at which sell side supply and buy side demand meet.<br/><br/>

 

Investors wishing to buy or sell North River Resources Plc shares (http://www.assetmatch.com/app/OurCompanies/CompanyProfile?companyId=1479) should register with Asset Match, where further details of the service together with information about North River Resources historic trades can be viewed.<br/><br/>

 

Important information: Investors considering using Asset Match should be aware that whilst the platform does try to make all procedures as simple as possible, it may still be difficult to realise the value of their share capital, primarily as there is a comparatively smaller audience of investors associated with not being listed on a recognised Investment exchange.
